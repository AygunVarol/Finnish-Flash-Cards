@app.route('/')
def index():
    return render_template('index.html', themes=list(FLASHCARDS.keys()))

@app.route('/start_session', methods=['POST'])
def start_session():
    theme = request.json['theme']
    session['theme'] = theme
    session['shown'] = []
    session['correct'] = 0
    session['wrong'] = 0
    return jsonify({"total": len(FLASHCARDS[theme])})

@app.route('/get_card')
def get_card():
    theme = session.get('theme')
    shown = session.get('shown', [])
    available = [i for i in range(len(FLASHCARDS[theme])) if i not in shown]
    if not available:
        return jsonify({"end": True, "correct": session.get('correct', 0), "wrong": session.get('wrong', 0), "total": len(FLASHCARDS[theme])})
    idx = random.choice(available)
    session['current_idx'] = idx
    session.modified = True
    return jsonify({"fi": FLASHCARDS[theme][idx]['fi'], "en": FLASHCARDS[theme][idx]['en'], "progress": len(shown)+1, "total": len(FLASHCARDS[theme])})

@app.route('/mark', methods=['POST'])
def mark():
    knew = request.json['knew']
    theme = session.get('theme')
    idx = session.get('current_idx')
    if idx not in session['shown']:
        session['shown'].append(idx)
        if knew:
            session['correct'] += 1
        else:
            session['wrong'] += 1
    session.modified = True
    return jsonify({"progress": len(session['shown']), "total": len(FLASHCARDS[theme]), "correct": session['correct'], "wrong": session['wrong']})

@app.route('/restart')
def restart():
    session.clear()
    return jsonify({"ok": True})

